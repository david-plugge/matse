	<p>
		Das duale Studium verknüpft <q>Angewandte Mathematik und Informatik</q> an der FH Aachen mit dem
		praktischen Training als mathematisch-technischer Softwareentwickler. Das Ganze ist wie eine perfekte
		Mischung aus Mathe und Code - sozusagen die Batman und Robin der IT-Welt!
	</p>

	<p>
		Hier gibt's keine öden Berufsschulstunden - die Ausbildungsinhalte sind voll im Studium
		integriert. Die FH und dein Ausbildungsbetrieb sind die coolen Doppelspieler in diesem Team.
	</p>

	<p>
		Aber was bedeutet eigentlich <q>angewandte Mathematik und Informatik</q>? Ganz einfach: MATSEs
		sind die Helden, die überall gebraucht werden. Ob Robotersteuerung, coole Apps fürs Handy oder
		Simulationen am Computer - die lösen überall Probleme.
	</p>

	<p>
		<q>Angewandt</q> heißt hier nicht, dass wir den ganzen Tag in Büchern versinken. Nein, wir packen
		die Probleme an, übersetzen sie in Mathematik, machen daraus coole Modelle und setzen die dann in
		Software um. Alles wird praktisch und erleichtert das Lernen - denn wer will schon langweilige Theorie?
	</p>

	<p>Und jetzt zu den Vorteilen von MATSE:</p>

	<ul>
		<li>Du bist Azubi und gleichzeitig Student.</li>
		<li>Zwei Abschlüsse in drei Jahren.</li>
		<li>Dein Ausbildungsplatz ist dein Garant für den Studienplatz.</li>
		<li>Das Gehalt aus der Ausbildung hilft finanziell durch die Studienzeit.</li>
		<li>
			Top Studienbedingungen: Kleine Gruppen, persönliche Interaktion mit Profs und Kommilitonen.
		</li>
		<li>Super Berufschancen warten auf dich.</li>
		<li>Lust auf einen Master? Du kannst dich an allen europäischen Unis bewerben!</li>
	</ul>

	<p>
		MATSE zu werden ist der perfekte Kickstart für deine Zukunft. Ob als praktischer Entwickler oder
		als akademischer Forscher - die Ausbildungsbetriebe haben alles, von Forschung an der Uni bis zu
		spannenden Projekten in regionalen IT-Teams. Deine Zukunft ist sicher, also worauf wartest du
		noch?
	</p>

